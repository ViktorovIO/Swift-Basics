enum Sex {
    case male
    case female
}
var sex: Sex = .male
print("Пол: \(sex)\n")

var sexCategory: String = getSexCategory(sex: sex, age: age)
var ageContent: String = getAgeContent(age: age, sexCategory: sexCategory)

