func getSexCategory(sex: Sex, age: Int) -> String {
    if (age >= 3 && age < 18) {
        return sex == .male ? "мальчиков" : "девочек"
    } else if (age >= 18) {
        return sex == .male ? "мужчин" : "женщин"
    }
    
    return ""
}

func getAgeContent(age: Int, sexCategory: String) -> String {
    switch (age) {
    case 3...17:
        return "Детский контент для \(sexCategory)"
    case 18...65:
        return "Взрослый контент для \(sexCategory)"
    case 66...:
        return "Контент для \(sexCategory) пожилого возраста"
    default:
        return "Нет подходящего контента"
    }
}
